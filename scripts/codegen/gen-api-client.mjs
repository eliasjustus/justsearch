#!/usr/bin/env node
/**
 * Tempdoc 583 §D.3d — typed FE API-route client, generated from the route manifest.
 *
 * 583 §D.3a made the Head's HTTP surface machine-inspectable (GET /api/meta/routes). This projects
 * that manifest into a typed TypeScript route table so FE call sites reference a checked constant
 * (`apiPath('GET /api/status')`) instead of a bare string literal — a typo or a removed route becomes
 * a compile error. The generated table is the SINGLE source the FE imports; it is not hand-edited.
 *
 * The manifest is a RUNTIME enumeration of the live Javalin router (there is no static route source
 * to read at build time), so generation is two-step:
 *
 *   1. Capture (needs a running backend, once per route-surface change):
 *        node scripts/codegen/gen-api-client.mjs --from-live=http://127.0.0.1:<apiPort>
 *      → writes the committed snapshot modules/ui-web/src/api/generated/route-manifest.snapshot.json AND the generated client.
 *   2. Regenerate (offline, from the committed snapshot — this is what CI runs):
 *        node scripts/codegen/gen-api-client.mjs            # write the client
 *        node scripts/codegen/gen-api-client.mjs --check    # exit non-zero on drift
 *
 * Honest scope (the user accepted this as future-proofing, tempdoc 583 §D.7): the committed snapshot
 * is a point-in-time capture and can drift if routes change without a re-capture; the offline --check
 * only guarantees the GENERATED CLIENT matches the SNAPSHOT, not that the snapshot matches the live
 * server (CI has no live backend — ADR-0026). Re-run --from-live after changing the route surface.
 */

import { existsSync, mkdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath, pathToFileURL } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);
const REPO_ROOT = join(__dirname, '..', '..');
// The captured route snapshot is codegen INPUT DATA (not a canonical wire-record schema), so it lives
// next to the generated client — NOT under SSOT/schemas/, which is mirrored into the ui classpath and
// served by SchemaController (a route snapshot is not a schema).
const SNAPSHOT_PATH = join(
  REPO_ROOT,
  'modules',
  'ui-web',
  'src',
  'api',
  'generated',
  'route-manifest.snapshot.json',
);
const OUT_PATH = join(
  REPO_ROOT,
  'modules',
  'ui-web',
  'src',
  'api',
  'generated',
  'apiRoutes.ts',
);

/** Render the typed client TS from a manifest object ({ schemaVersion, count, routes:[...] }). */
export function renderClient(manifest) {
  const routes = [...(manifest.routes ?? [])].sort((a, b) => {
    const ka = `${a.method} ${a.path}`;
    const kb = `${b.method} ${b.path}`;
    return ka < kb ? -1 : ka > kb ? 1 : 0;
  });
  const seen = new Set();
  const entries = [];
  for (const r of routes) {
    const key = `${r.method} ${r.path}`;
    if (seen.has(key)) continue; // a method+path is unique; guard defensively
    seen.add(key);
    const caps = (r.requiredCapabilities ?? []).map((c) => JSON.stringify(c)).join(', ');
    entries.push(
      `  ${JSON.stringify(key)}: { method: ${JSON.stringify(r.method)}, ` +
        `path: ${JSON.stringify(r.path)}, cohort: ${JSON.stringify(r.cohort)}, ` +
        `requiredCapabilities: [${caps}] },`,
    );
  }
  return `// GENERATED by scripts/codegen/gen-api-client.mjs from modules/ui-web/src/api/generated/route-manifest.snapshot.json
// DO NOT EDIT. Re-capture with: node scripts/codegen/gen-api-client.mjs --from-live=http://127.0.0.1:<apiPort>
// Then regenerate offline with: node scripts/codegen/gen-api-client.mjs
//
// Tempdoc 583 §D.3d — a typed projection of the live route manifest (GET /api/meta/routes). FE call
// sites reference a checked route key instead of a bare string, so a typo/removed route is a compile
// error. The required capability per route comes from the one RouteCapabilityPolicy authority.

export interface ApiRoute {
  readonly method: string;
  readonly path: string;
  readonly cohort: string;
  readonly requiredCapabilities: readonly string[];
}

/** Every route the Head exposes, keyed by "<METHOD> <path>". Snapshot schema v${manifest.schemaVersion ?? '?'}. */
export const API_ROUTES = {
${entries.join('\n')}
} as const satisfies Record<string, ApiRoute>;

export type ApiRouteKey = keyof typeof API_ROUTES;

/** The path template for a route key (compile error on an unknown key). */
export function apiPath(key: ApiRouteKey): string {
  return API_ROUTES[key].path;
}

/** The runtime capabilities a route requires (empty = always available / GET-exempt). */
export function requiredCapabilities(key: ApiRouteKey): readonly string[] {
  return API_ROUTES[key].requiredCapabilities;
}
`;
}

async function captureFromLive(baseUrl) {
  const url = `${baseUrl.replace(/\/$/, '')}/api/meta/routes`;
  const res = await fetch(url, { headers: { accept: 'application/json' } });
  if (!res.ok) throw new Error(`capture failed: GET ${url} -> HTTP ${res.status}`);
  const manifest = await res.json();
  if (!manifest || !Array.isArray(manifest.routes)) {
    throw new Error(`capture failed: ${url} returned no routes array`);
  }
  writeFileSync(SNAPSHOT_PATH, JSON.stringify(manifest, null, 2) + '\n');
  console.log(`captured ${manifest.routes.length} routes -> ${SNAPSHOT_PATH}`);
  return manifest;
}

function readSnapshot() {
  if (!existsSync(SNAPSHOT_PATH)) {
    throw new Error(
      `no committed snapshot at ${SNAPSHOT_PATH}; run --from-live=<baseUrl> against a running backend first`,
    );
  }
  return JSON.parse(readFileSync(SNAPSHOT_PATH, 'utf8'));
}

async function main() {
  const args = process.argv.slice(2);
  const check = args.includes('--check');
  const fromLive = args.find((a) => a.startsWith('--from-live'));

  let manifest;
  if (fromLive) {
    const baseUrl = fromLive.includes('=') ? fromLive.split('=')[1] : args[args.indexOf(fromLive) + 1];
    if (!baseUrl) throw new Error('--from-live requires a base URL, e.g. --from-live=http://127.0.0.1:8080');
    manifest = await captureFromLive(baseUrl);
  } else {
    manifest = readSnapshot();
  }

  const next = renderClient(manifest);
  if (check) {
    const current = existsSync(OUT_PATH) ? readFileSync(OUT_PATH, 'utf8') : '';
    if (current !== next) {
      console.error(`DRIFT: ${OUT_PATH} is stale. Run: node scripts/codegen/gen-api-client.mjs`);
      process.exit(1);
    }
    console.log('apiRoutes.ts up to date');
    return;
  }
  mkdirSync(dirname(OUT_PATH), { recursive: true });
  writeFileSync(OUT_PATH, next);
  console.log(`wrote ${OUT_PATH} (${(manifest.routes ?? []).length} routes)`);
}

const isMain = process.argv[1] && import.meta.url === pathToFileURL(process.argv[1]).href;
if (isMain) {
  main().catch((e) => {
    console.error(e.message ?? e);
    process.exit(1);
  });
}
