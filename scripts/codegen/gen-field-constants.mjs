#!/usr/bin/env node
/**
 * Tempdoc 594 Move 1a — generate the build-time FIELD constants the FE displays as facts,
 * FROM the SSOT field catalog, so a displayed build-time fact (the embedding vector dimension)
 * cannot drift from its single authority.
 *
 * The catalog `SSOT/catalogs/fields.v1.json` is the ONE source of the embedding vector dimension
 * (the `vector` field's `vector.dimension`). This script projects that into:
 *   - TS: modules/ui-web/src/api/generated/field-constants.ts (the FE chip strip)
 * so the constant IS the catalog — a wrong literal like "Embeddings 384-d" is impossible by
 * construction (594 §9.2/§9.4, the Generate rung of the 557 ladder). Mirrors
 * gen-liveness-constants.mjs (575 §17 Face A): single source, `--check` idempotency gate.
 *
 * Usage:
 *   node scripts/codegen/gen-field-constants.mjs           # write
 *   node scripts/codegen/gen-field-constants.mjs --check   # exit non-zero on drift
 */

import { existsSync, mkdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, join, relative } from 'node:path';
import { fileURLToPath, pathToFileURL } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);
const REPO_ROOT = join(__dirname, '..', '..');

const CATALOG = join(REPO_ROOT, 'SSOT', 'catalogs', 'fields.v1.json');
const TS_OUT = join(
  REPO_ROOT,
  'modules',
  'ui-web',
  'src',
  'api',
  'generated',
  'field-constants.ts',
);

/**
 * Read the embedding vector dimension from the field catalog. The `vector` field carries the
 * canonical embedding dimension; `chunk_vector` must agree (both index the same model's vectors)
 * — a divergence is a catalog error, surfaced HERE at generation. `catalog` is injectable so the
 * selection law is unit-testable.
 */
function readEmbedVectorDim(catalog = JSON.parse(readFileSync(CATALOG, 'utf8'))) {
  const fields = catalog.fields ?? catalog;
  if (!Array.isArray(fields)) {
    throw new Error('fields.v1.json: expected a `fields` array');
  }
  const dimOf = (id) => {
    const f = fields.find((x) => x && x.id === id);
    if (!f) throw new Error(`fields.v1.json: no field with id "${id}"`);
    const dim = f.vector && f.vector.dimension;
    if (typeof dim !== 'number' || !Number.isInteger(dim) || dim <= 0) {
      throw new Error(`fields.v1.json: field "${id}" has no positive-integer vector.dimension (got ${dim})`);
    }
    return dim;
  };
  const vectorDim = dimOf('vector');
  const chunkDim = dimOf('chunk_vector');
  if (vectorDim !== chunkDim) {
    throw new Error(
      `fields.v1.json: vector.dimension (${vectorDim}) != chunk_vector.dimension (${chunkDim}) — ` +
        `the embedding dimension is one value; the FE fact derives from one authority`,
    );
  }
  return { embedVectorDim: vectorDim };
}

function renderTs({ embedVectorDim }) {
  return `/**
 * AUTO-GENERATED by scripts/codegen/gen-field-constants.mjs (tempdoc 594 Move 1a).
 * Source: SSOT/catalogs/fields.v1.json (the "vector" field's vector.dimension).
 *
 * The single source of truth for the embedding vector dimension the FE displays as a fact
 * ("Embeddings <dim>-d"). Do NOT edit by hand; re-generate with:
 *   node scripts/codegen/gen-field-constants.mjs
 */

export const EMBED_VECTOR_DIM = ${embedVectorDim};
`;
}

function main() {
  const check = process.argv.includes('--check');
  const dims = readEmbedVectorDim();
  const outputs = [{ path: TS_OUT, content: renderTs(dims) }];

  if (check) {
    const drift = [];
    for (const { path, content } of outputs) {
      const before = existsSync(path) ? readFileSync(path, 'utf8') : null;
      if (before !== content) drift.push(relative(REPO_ROOT, path));
    }
    if (drift.length > 0) {
      console.error('[gen-field-constants] CHECK FAILED: output drifted from committed files:');
      for (const d of drift) console.error('  - ' + d);
      console.error('Re-run `node scripts/codegen/gen-field-constants.mjs`, inspect, commit the regen.');
      process.exit(1);
    }
    console.log('[gen-field-constants] check passed — output matches committed files');
    return;
  }

  for (const { path, content } of outputs) {
    mkdirSync(dirname(path), { recursive: true });
    writeFileSync(path, content, 'utf8');
  }
  console.log(
    '[gen-field-constants] wrote:\n  ' + outputs.map((o) => relative(REPO_ROOT, o.path)).join('\n  '),
  );
}

const isMain = process.argv[1] && import.meta.url === pathToFileURL(process.argv[1]).href;
if (isMain) {
  try {
    main();
  } catch (err) {
    console.error('[gen-field-constants] failed:', err);
    process.exit(1);
  }
}

export { TS_OUT, readEmbedVectorDim };
