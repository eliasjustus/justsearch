#!/usr/bin/env node
/**
 * Tempdoc 575 §17 Face A — generate the action-lifecycle liveness window
 * constants FROM the register, into both the worker (Java) and the FE (TS).
 *
 * The register `governance/observed-happening.v1.json` is the ONE source of the
 * liveness window (the worker heartbeat interval, the FE display-freshness
 * window, the reaper window). This script projects that block into:
 *   - Java: modules/worker-core/.../liveness/LivenessWindows.java  (worker + reaper)
 *   - TS:   modules/ui-web/src/api/generated/liveness-constants.ts (FE display)
 * so the constants ARE the register — drift is impossible by construction. This
 * REPLACES the old `observed-happening/liveness-window-coherent` drift-parsing
 * gate (which detected drift after the fact) with generation + an idempotency
 * gate (`check-liveness-constants-regen.mjs`) + a single-authority mandate gate.
 *
 * The ordering law (`heartbeat < displayStale <= reaper`, `displayStale >=
 * 3*heartbeat`) is validated HERE, at generation — not re-derived downstream.
 *
 * Usage:
 *   node scripts/codegen/gen-liveness-constants.mjs           # write
 *   node scripts/codegen/gen-liveness-constants.mjs --check   # exit non-zero on drift
 */

import { existsSync, mkdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, join, relative } from 'node:path';
import { fileURLToPath, pathToFileURL } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);
const REPO_ROOT = join(__dirname, '..', '..');

const REGISTER = join(REPO_ROOT, 'governance', 'observed-happening.v1.json');
const JAVA_OUT = join(
  REPO_ROOT,
  'modules',
  'worker-core',
  'src',
  'main',
  'java',
  'io',
  'justsearch',
  'indexerworker',
  'liveness',
  'LivenessWindows.java',
);
const TS_OUT = join(
  REPO_ROOT,
  'modules',
  'ui-web',
  'src',
  'api',
  'generated',
  'liveness-constants.ts',
);

/**
 * Read the ONE liveness WINDOW from the register — the concept whose `liveness` block declares the
 * window triple (a numeric `heartbeatMs`/`displayStaleMs`/`reaperStaleMs`). Only the heartbeat-lease
 * model carries a window; polled-state concepts (575 §17 Face C: ai-install / pack-import) declare
 * `liveness.model` with NO window, so they are correctly skipped here. Selecting on "any liveness
 * block" would wrongly count them — the regression Face C introduced once it added a `liveness.model`
 * facet to each stateful concept. `register` is injectable so the selection law is unit-testable.
 */
function readLivenessWindow(register = JSON.parse(readFileSync(REGISTER, 'utf8'))) {
  const windowBearing = (register.concepts ?? []).filter(
    (c) => c.liveness && typeof c.liveness === 'object' && typeof c.liveness.heartbeatMs === 'number',
  );
  if (windowBearing.length !== 1) {
    throw new Error(
      `expected exactly ONE concept with a liveness WINDOW (a numeric heartbeatMs), found ` +
        `${windowBearing.length} (${windowBearing.map((c) => c.id).join(', ')}) — the generator ` +
        `projects a single window; polled-state concepts declare liveness.model with no window`,
    );
  }
  const concept = windowBearing[0];
  const lv = concept.liveness;
  const heartbeatMs = lv.heartbeatMs;
  const displayStaleMs = lv.displayStaleMs;
  const reaperStaleMs = lv.reaperStaleMs;
  for (const [k, v] of Object.entries({ heartbeatMs, displayStaleMs, reaperStaleMs })) {
    if (typeof v !== 'number' || !Number.isInteger(v) || v <= 0) {
      throw new Error(`liveness.${k} must be a positive integer (got ${v})`);
    }
  }
  // The ordering law — validated once, here, at generation.
  if (!(heartbeatMs < displayStaleMs && displayStaleMs <= reaperStaleMs)) {
    throw new Error(
      `liveness ordering violated: require heartbeat(${heartbeatMs}) < displayStale(${displayStaleMs}) <= reaper(${reaperStaleMs})`,
    );
  }
  if (!(displayStaleMs >= 3 * heartbeatMs)) {
    throw new Error(
      `liveness margin violated: require displayStale(${displayStaleMs}) >= 3 * heartbeat(${heartbeatMs})`,
    );
  }
  return { conceptId: concept.id, heartbeatMs, displayStaleMs, reaperStaleMs };
}

/** Java `long` literal with digit-group underscores (e.g. 300000 -> "300_000L"). */
function javaLong(n) {
  return n.toLocaleString('en-US').replace(/,/g, '_') + 'L';
}
/** TS number literal with digit-group underscores (e.g. 90000 -> "90_000"). */
function tsNumber(n) {
  return n.toLocaleString('en-US').replace(/,/g, '_');
}

function renderJava({ conceptId, heartbeatMs, displayStaleMs, reaperStaleMs }) {
  return `/*
 * AUTO-GENERATED by scripts/codegen/gen-liveness-constants.mjs (tempdoc 575 §17 Face A).
 * Source: governance/observed-happening.v1.json (the "${conceptId}" concept's liveness block).
 *
 * The single source of truth for the action-lifecycle liveness window, shared by the
 * worker heartbeat loop (IndexingLoop) and the reaper (KnowledgeServer). The FE display
 * freshness window is the same value, generated into liveness-constants.ts. Do NOT edit by
 * hand (the constant single-authority gate forbids a hand-authored copy); re-generate with:
 *   node scripts/codegen/gen-liveness-constants.mjs
 */
package io.justsearch.indexerworker.liveness;

/** Generated liveness window constants — see file header. */
public final class LivenessWindows {
  private LivenessWindows() {}

  /** Worker heartbeat cadence: refresh PROCESSING rows' last_updated at least this often. */
  public static final long HEARTBEAT_INTERVAL_MS = ${javaLong(heartbeatMs)};

  /** FE display-freshness window: a record is "live" only if beaten within this window. */
  public static final long DISPLAY_STALE_MS = ${javaLong(displayStaleMs)};

  /** Reaper window: a PROCESSING row older than this with no heartbeat is an orphan. */
  public static final long REAPER_STALE_MS = ${javaLong(reaperStaleMs)};
}
`;
}

function renderTs({ conceptId, heartbeatMs, displayStaleMs, reaperStaleMs }) {
  return `/**
 * AUTO-GENERATED by scripts/codegen/gen-liveness-constants.mjs (tempdoc 575 §17 Face A).
 * Source: governance/observed-happening.v1.json (the "${conceptId}" concept's liveness block).
 *
 * The single source of truth for the action-lifecycle liveness window, shared with the
 * worker (LivenessWindows.java). Do NOT edit by hand; re-generate with:
 *   node scripts/codegen/gen-liveness-constants.mjs
 */

export const HEARTBEAT_INTERVAL_MS = ${tsNumber(heartbeatMs)};
export const DISPLAY_STALE_MS = ${tsNumber(displayStaleMs)};
export const REAPER_STALE_MS = ${tsNumber(reaperStaleMs)};
`;
}

function main() {
  const check = process.argv.includes('--check');
  const window = readLivenessWindow();
  const outputs = [
    { path: JAVA_OUT, content: renderJava(window) },
    { path: TS_OUT, content: renderTs(window) },
  ];

  if (check) {
    const drift = [];
    for (const { path, content } of outputs) {
      const before = existsSync(path) ? readFileSync(path, 'utf8') : null;
      if (before !== content) drift.push(relative(REPO_ROOT, path));
    }
    if (drift.length > 0) {
      console.error('[gen-liveness-constants] CHECK FAILED: output drifted from committed files:');
      for (const d of drift) console.error('  - ' + d);
      console.error('Re-run `node scripts/codegen/gen-liveness-constants.mjs`, inspect, commit the regen.');
      process.exit(1);
    }
    console.log('[gen-liveness-constants] check passed — output matches committed files');
    return;
  }

  for (const { path, content } of outputs) {
    mkdirSync(dirname(path), { recursive: true });
    writeFileSync(path, content, 'utf8');
  }
  console.log(
    '[gen-liveness-constants] wrote:\n  ' +
      outputs.map((o) => relative(REPO_ROOT, o.path)).join('\n  '),
  );
}

const isMain = process.argv[1] && import.meta.url === pathToFileURL(process.argv[1]).href;
if (isMain) {
  try {
    main();
  } catch (err) {
    console.error('[gen-liveness-constants] failed:', err);
    process.exit(1);
  }
}

export { JAVA_OUT, TS_OUT, readLivenessWindow };
