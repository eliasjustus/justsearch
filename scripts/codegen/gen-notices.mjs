#!/usr/bin/env node
/**
 * Tempdoc 632 — attribution-as-projection.
 *
 * Single authority for "what JustSearch redistributes and under what license" is a set of
 * declarations (the jk1 dependency report, the npm license dump, model-registry.v2.json, and the
 * native-binary manifests). This generator PROJECTS them into the committed `NOTICE` +
 * `THIRD_PARTY_NOTICES`; it is never hand-edited. `--check` re-projects and fails on drift (the
 * closure/equality half, conforming to the repo's gen-*.mjs / check-*-regen pattern), plus a set of
 * presence checks (every model has a license; the two registry copies match; every bundled Tesseract
 * DLL is attributed). Run after `generateLicenseReport` + `license-checker` have produced their dumps.
 *
 * Usage:  node scripts/codegen/gen-notices.mjs [--check]
 */

import { existsSync, mkdirSync, readFileSync, writeFileSync, readdirSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath, pathToFileURL } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = join(__dirname, '..', '..');

// --- Inputs (declarations) ---------------------------------------------------
const JK1_REPORT = join(REPO_ROOT, 'build', 'reports', 'licenses', 'third-party-licenses.json');
const NPM_DUMP = join(REPO_ROOT, 'build', 'npm-licenses.json');
const CARGO_DUMP = join(REPO_ROOT, 'build', 'cargo-licenses.json');
const REGISTRY_PROD = join(REPO_ROOT, 'modules', 'ui', 'src', 'main', 'resources', 'ai', 'model-registry.v2.json');
const REGISTRY_TEST = join(REPO_ROOT, 'modules', 'configuration', 'src', 'test', 'resources', 'ai', 'model-registry.v2.json');
const TESS_CORE = join(REPO_ROOT, 'packaging', 'runtime', 'tesseract-windows.v1.json');
const TESS_DLLS = join(REPO_ROOT, 'packaging', 'runtime', 'tesseract-bundled-libraries.v1.json');
const OVERRIDES = join(REPO_ROOT, 'scripts', 'codegen', 'license-overrides.json');
const ALLOWED_LICENSES = join(REPO_ROOT, 'config', 'allowed-licenses.json');
const NATIVE_ACCEPTANCES = join(REPO_ROOT, 'config', 'native-license-acceptances.json');
const NATIVE_BIN_TESS = join(REPO_ROOT, 'native-bin', 'tesseract'); // gitignored; present only where staged

// --- Outputs (projections) ---------------------------------------------------
const NOTICE_OUT = join(REPO_ROOT, 'NOTICE');
const TPN_OUT = join(REPO_ROOT, 'THIRD_PARTY_NOTICES');
// Installer-bundled model attribution — the Gradle `generateOnnxNotice` task copies this committed,
// registry-projected file into the bundle instead of hardcoding a heredoc (tempdoc 632).
const NOTICE_MODELS_OUT = join(REPO_ROOT, 'packaging', 'runtime', 'NOTICE-MODELS.txt');
// Canonical legal doc — the model/license TABLE is a generated region (tempdoc 632), projected from the
// registry into a marked span so it can't drift (the same closure as the notices).
const LEGAL_DOC = join(REPO_ROOT, 'docs', 'reference', 'legal', 'ai-runtime-and-model-redistribution.md');
const LEGAL_MARK_START = '<!-- GENERATED:MODEL_LICENSES:BEGIN — do not edit; run: node scripts/codegen/gen-notices.mjs -->';
const LEGAL_MARK_END = '<!-- GENERATED:MODEL_LICENSES:END -->';

const GEN_BANNER =
  'AUTO-GENERATED by scripts/codegen/gen-notices.mjs — do NOT edit by hand.\n' +
  'Regenerate: node scripts/codegen/gen-notices.mjs (after `gradlew generateLicenseReport` + `license-checker`).';

const readJson = (p) => JSON.parse(readFileSync(p, 'utf8'));

/** JVM deps from the jk1 report, with dual-licensed deps claimed under their permissive override. */
function loadJvm(report = readJson(JK1_REPORT), overrides = readJson(OVERRIDES)) {
  const ov = overrides.jvmModuleLicense || {};
  const seen = new Map();
  for (const d of report.dependencies || []) {
    const license = ov[d.moduleName] || d.moduleLicense || 'UNKNOWN';
    const key = `${d.moduleName}@${d.moduleVersion}`;
    seen.set(key, { name: d.moduleName, version: d.moduleVersion || '', license });
  }
  return [...seen.values()].sort((a, b) => a.name.localeCompare(b.name) || a.version.localeCompare(b.version));
}

/** Frontend npm deps from the license-checker dump (key = name@version). */
function loadNpm(dump = readJson(NPM_DUMP)) {
  return Object.entries(dump)
    .map(([k, v]) => {
      const at = k.lastIndexOf('@');
      return {
        name: k.slice(0, at),
        version: k.slice(at + 1),
        license: Array.isArray(v.licenses) ? v.licenses.join(' OR ') : String(v.licenses || 'UNKNOWN'),
      };
    })
    .sort((a, b) => a.name.localeCompare(b.name) || a.version.localeCompare(b.version));
}

/** Rust/Cargo deps of the Tauri desktop shell (statically linked → redistributed). */
function loadCargo(dump = readJson(CARGO_DUMP)) {
  return [...dump].sort((a, b) => a.name.localeCompare(b.name) || a.version.localeCompare(b.version));
}

/** Bundled ML models — the registry is the SSOT for their licenses. */
function loadModels(registry = readJson(REGISTRY_PROD)) {
  return (registry.packages || [])
    .map((p) => ({ id: p.id, label: p.label, license: p.license, termsUrl: p.termsUrl, targetDir: p.targetDir }))
    .sort((a, b) => a.id.localeCompare(b.id));
}

function loadNative(overrides = readJson(OVERRIDES), tessCore = readJson(TESS_CORE), tessDlls = readJson(TESS_DLLS)) {
  return { runtimes: overrides.nativeRuntimes || [], tessCore, tessDlls };
}

// --- Rendering ---------------------------------------------------------------
function renderThirdParty(jvm, npm, models, native, cargo = []) {
  const L = [];
  L.push('JustSearch — Third-Party Notices');
  L.push('================================');
  L.push('');
  L.push(GEN_BANNER);
  L.push('');
  L.push('Licenses of third-party components redistributed with JustSearch.');
  L.push('');
  L.push('== Java / JVM dependencies ==');
  for (const d of jvm) L.push(`  ${d.name} ${d.version} — ${d.license}`);
  L.push('');
  L.push('== Frontend (npm) dependencies ==');
  for (const d of npm) L.push(`  ${d.name} ${d.version} — ${d.license}`);
  L.push('');
  L.push('== Rust / Cargo (desktop shell) dependencies ==');
  for (const d of cargo) L.push(`  ${d.name} ${d.version} — ${d.license}`);
  L.push('');
  L.push('== Bundled ML models ==');
  for (const m of models) L.push(`  ${m.label} (${m.id}) — ${m.license} — ${m.termsUrl}`);
  L.push('');
  L.push('== Native runtime binaries ==');
  for (const r of native.runtimes) {
    L.push(`  ${r.name} — ${r.license}`);
    L.push(`    ${r.ref}${r.bundled ? `; bundled: ${r.bundled}` : ''}`);
  }
  L.push('');
  L.push(`  Tesseract OCR ${native.tessCore.version} — ${native.tessCore.license}; English tessdata — Apache-2.0`);
  L.push('  Bundled Windows runtime libraries (MSYS2/mingw-w64):');
  // Group DLL entries by library to keep the notice compact + deterministic.
  const byLib = new Map();
  for (const e of [...native.tessDlls.libraries].sort((a, b) => a.library.localeCompare(b.library))) {
    if (!byLib.has(e.library)) byLib.set(e.library, e.license);
  }
  for (const [lib, lic] of byLib) L.push(`    ${lib} — ${lic}`);
  L.push('');
  return L.join('\n') + '\n';
}

/** Installer-bundled model attribution, projected from the registry (replaces the old heredoc). */
function renderModelsNotice(models) {
  const L = [];
  L.push('Bundled Search & Chat Models');
  L.push('============================');
  L.push('');
  L.push(GEN_BANNER);
  L.push('');
  L.push('Pre-trained models bundled for indexing, retrieval, reranking, entity');
  L.push('recognition, citation scoring, and chat. Each is redistributed under the');
  L.push('license below (single-sourced from ai/model-registry.v2.json).');
  L.push('');
  for (const m of models.filter((x) => x.id !== 'cuda-runtime')) {
    L.push(`${m.label} (${m.id})`);
    if (m.targetDir) L.push(`  path: ${m.targetDir}`);
    L.push(`  license: ${m.license}`);
    if (m.termsUrl) L.push(`  terms: ${m.termsUrl}`);
    L.push('');
  }
  return L.join('\n');
}

/** The canonical-doc model/license table, projected from the registry (Model | License | Source). */
function renderModelLicenseTable(models) {
  const rows = models
    .filter((m) => m.id !== 'cuda-runtime')
    .map((m) => {
      const src = m.termsUrl ? `[${m.termsUrl.replace(/^https?:\/\//, '')}](${m.termsUrl})` : '—';
      return `| ${m.label} (\`${m.id}\`) | ${m.license} | ${src} |`;
    });
  return ['| Model | License | Source |', '|-------|---------|--------|', ...rows].join('\n');
}

/** Splice the generated model-license table into the legal doc's marked region (llmstxt-style). */
function injectLegalDocTable(models, existing = readFileSync(LEGAL_DOC, 'utf8')) {
  const s = existing.indexOf(LEGAL_MARK_START);
  const e = existing.indexOf(LEGAL_MARK_END);
  if (s === -1 || e === -1) {
    throw new Error(`${LEGAL_DOC} is missing the MODEL_LICENSES markers — add them, then re-run gen-notices`);
  }
  const before = existing.slice(0, s);
  const after = existing.slice(e + LEGAL_MARK_END.length);
  return `${before}${LEGAL_MARK_START}\n${renderModelLicenseTable(models)}\n${LEGAL_MARK_END}${after}`;
}

