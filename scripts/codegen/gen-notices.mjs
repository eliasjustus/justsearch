/**
 * True if `license` is strong copyleft (GPL/AGPL/SSPL) with NO permissive alternative and NO linking
 * exception — i.e. the one class that needs review before redistribution in a permissive bundle.
 * LGPL/MPL/EPL (weak/file-level, fine as separate components) and GPL-WITH-exception (GCC runtime) and
 * any dual that offers a permissive option are NOT flagged.
 */
function isStrongCopyleft(license) {
  if (!license || license === 'UNKNOWN') return false; // UNKNOWN is a separate check
  const strongCopyleft = /\b(?:A?GPL|SSPL)\b/i; // \bGPL does NOT match "LGPL" (no word boundary before G)
  const exceptionOk = /WITH .*exception/i;
  const permissiveAlt = /\b(?:MIT|BSD|Apache|MPL|ISC|Zlib|0BSD|Unicode|CC0|Unlicense)\b/i;
  return strongCopyleft.test(license) && !exceptionOk.test(license) && !permissiveAlt.test(license);
}

// The 5 native-binary dispositions. FIRING ones require a dated acceptance (tempdoc 632 disposition gate).
const NATIVE_DISPOSITIONS = new Set([
  'permissive',
  'weak-copyleft-dynamic', // LGPL/MPL as a separate replaceable DLL — ship the text, no waiver needed
  'copyleft-with-linking-exception', // GCC runtime — exception permits non-GPL linking; ship the exception text
  'strong-copyleft', // GPL/AGPL with no permissive alternative — FIRING
  'restricted', // EULA/SLA/field-of-use — FIRING
]);
const FIRING_DISPOSITIONS = new Set(['strong-copyleft', 'restricted']);

/**
 * The native-binary policy gate (tempdoc 632). The dependency-tree analogue is jk1 `checkLicense` over
 * config/allowed-licenses.json; this is the native equivalent over the committed manifests (so it runs in CI
 * without native-bin staged). Disposition is an EXPLICIT declared field — never regex-derived (FreeType
 * FTL-OR-GPL, GCC WITH-exception, libidn2 LGPL-OR-GPL all defeat naive classification). Three assertions:
 *   (1) no silent default — every native artifact carries a valid disposition;
 *   (2) firing requires acceptance — strong-copyleft|restricted needs a dated entry in the acceptance ledger;
 *   (3) no stale acceptance — every acceptance maps to a currently-firing artifact (a waiver can't outlive it).
 * Pure + injectable (mirrors the loaders) so the test drives it with crafted data.
 */
function nativeDispositionCheck(native = loadNative(), ledger = readJson(NATIVE_ACCEPTANCES)) {
  const errors = [];
  const accepted = new Set((ledger.acceptances || []).map((a) => a.artifact));
  // Redistributed third-party native surface = the Tesseract bundled DLLs + the nativeRuntimes block.
  // tessCore (the Tesseract engine pin) is Apache-2.0 and already covered by its libtesseract-5.dll row.
  const artifacts = [
    ...native.tessDlls.libraries.map((l) => ({ name: l.library, disposition: l.disposition })),
    ...(native.runtimes || []).map((r) => ({ name: r.name, disposition: r.disposition })),
  ];
  const firingNames = new Set();
  for (const a of artifacts) {
    if (!a.disposition || !NATIVE_DISPOSITIONS.has(a.disposition)) {
      errors.push(`native artifact '${a.name}' has missing/invalid disposition '${a.disposition}' — classify it in the manifest (one of: ${[...NATIVE_DISPOSITIONS].join(', ')})`);
      continue;
    }
    if (FIRING_DISPOSITIONS.has(a.disposition)) {
      firingNames.add(a.name);
      if (!accepted.has(a.name)) {
        errors.push(`native artifact '${a.name}' is ${a.disposition} (FIRING) with NO dated acceptance — add an entry to config/native-license-acceptances.json or remove the artifact`);
      }
    }
  }
  // (3) symmetric: no acceptance may outlive its firing artifact.
  for (const artifact of accepted) {
    if (!firingNames.has(artifact)) {
      errors.push(`acceptance for '${artifact}' has no matching firing native artifact — remove the stale entry from config/native-license-acceptances.json`);
    }
  }
  return errors;
}

// --- Presence checks (the closure half) --------------------------------------
function presenceChecks() {
  const errors = [];
  // 1. every model package declares a license
  const reg = readJson(REGISTRY_PROD);
  for (const p of reg.packages || []) {
    if (!p.license || !String(p.license).trim()) errors.push(`model package '${p.id}' has no license (632 SSOT)`);
  }
  // 2. the two registry copies are byte-identical (currently ungated dual-copy)
  if (readFileSync(REGISTRY_PROD, 'utf8') !== readFileSync(REGISTRY_TEST, 'utf8')) {
    errors.push('model-registry prod and test copies differ — keep them byte-identical');
  }
  // 2b. the gate allowlist's per-moduleName allows must match the NOTICE overrides' moduleNames
  //     (the dual-licensed / junrar / jcip special-cases live in BOTH files; this keeps them from drifting
  //     apart — the design's "no silent fork", as a check rather than full generation since the allowlist's
  //     permissive-license strings have a different change-reason and stay hand-authored).
  if (existsSync(ALLOWED_LICENSES) && existsSync(OVERRIDES)) {
    const allowNames = new Set(
      (readJson(ALLOWED_LICENSES).allowedLicenses || []).filter((e) => e.moduleName).map((e) => e.moduleName),
    );
    const overrideNames = new Set(Object.keys(readJson(OVERRIDES).jvmModuleLicense || {}));
    for (const n of overrideNames) {
      if (!allowNames.has(n)) errors.push(`'${n}' is in license-overrides.json but NOT allowlisted in config/allowed-licenses.json`);
    }
    for (const n of allowNames) {
      if (!overrideNames.has(n)) errors.push(`'${n}' is allowlisted by moduleName but has NO NOTICE override in license-overrides.json`);
    }
  }
  // 3. every bundled Tesseract DLL is attributed (skip-when-absent: native-bin is gitignored / installer-staged)
  if (existsSync(NATIVE_BIN_TESS)) {
    const table = new Set(readJson(TESS_DLLS).libraries.map((l) => l.dll));
    const actual = readdirSync(NATIVE_BIN_TESS).filter((f) => f.endsWith('.dll'));
    for (const dll of actual) {
      if (!table.has(dll)) errors.push(`Tesseract DLL '${dll}' is not attributed in tesseract-bundled-libraries.v1.json`);
    }
  }
  // 4. no UNKNOWN license slipped into the attribution, and no strong copyleft in any shipped tree.
  //    The jk1 gate only covers the JVM classpath, so this is the only copyleft guard on the npm + Cargo trees.
  //    Strong copyleft = GPL/AGPL/SSPL (not LGPL/MPL/EPL, which are fine as separate/dynamic components). A bare
  //    "GPL-...-WITH-...-exception" (e.g. the GCC runtime) is permitted; the native Tesseract table owns those.
  if (existsSync(JK1_REPORT) && existsSync(NPM_DUMP) && existsSync(CARGO_DUMP)) {
    for (const d of [...loadJvm(), ...loadNpm(), ...loadCargo()]) {
      if (d.license === 'UNKNOWN') {
        errors.push(`${d.name} ${d.version} has an UNKNOWN license — add an override in license-overrides.json`);
      } else if (isStrongCopyleft(d.license)) {
        errors.push(`${d.name} ${d.version} is strong copyleft with no permissive alternative (${d.license}) — review before redistribution`);
      }
    }
  }
  // 5. native-binary disposition gate — the policy half for the native axis (the jk1 gate covers only the JVM
  //    classpath; checks 1-4 cover presence/equality but not native redistribution POLICY). Closes the proven
  //    libjbig silent-GPL-ship blind spot: a FIRING native disposition (strong-copyleft/restricted) needs a dated
  //    acceptance, mirroring checkLicense's allowlist. Runs on the committed manifests → unconditional in CI.
  errors.push(...nativeDispositionCheck());
  return errors;
}

function build() {
  const jvm = loadJvm();
  const npm = loadNpm();
  const models = loadModels();
  const native = loadNative();
  const cargo = loadCargo();
  return [
    { path: NOTICE_OUT, content: renderNotice(models) },
    { path: TPN_OUT, content: renderThirdParty(jvm, npm, models, native, cargo) },
    { path: NOTICE_MODELS_OUT, content: renderModelsNotice(models) },
    { path: LEGAL_DOC, content: injectLegalDocTable(models) },
  ];
}

function main() {
  const check = process.argv.includes('--check');

  const presence = presenceChecks();
  if (presence.length) {
    console.error('NOTICE presence checks FAILED:');
    for (const e of presence) console.error(`  - ${e}`);
    process.exit(1);
  }

  const outputs = build();
  if (check) {
    const drift = [];
    for (const { path, content } of outputs) {
      const before = existsSync(path) ? readFileSync(path, 'utf8') : null;
      if (before !== content) drift.push(path);
    }
    if (drift.length) {
      console.error('NOTICE projection is STALE — these files drifted from their sources:');
      for (const p of drift) console.error(`  - ${p}`);
      console.error('Regenerate: node scripts/codegen/gen-notices.mjs');
      process.exit(1);
    }
    console.log('gen-notices --check: NOTICE + THIRD_PARTY_NOTICES are in sync.');
    return;
  }

  for (const { path, content } of outputs) {
    mkdirSync(dirname(path), { recursive: true });
    writeFileSync(path, content, 'utf8');
    console.log(`wrote ${path}`);
  }
}

const isMain = import.meta.url === pathToFileURL(process.argv[1]).href;
if (isMain) {
  try {
    main();
  } catch (e) {
    console.error(e.message);
    process.exit(1);
  }
}

export {
  loadJvm, loadNpm, loadModels, loadNative, loadCargo,
  renderNotice, renderThirdParty, renderModelsNotice, renderModelLicenseTable, injectLegalDocTable,
  presenceChecks, isStrongCopyleft, nativeDispositionCheck,
  NOTICE_OUT, TPN_OUT, NOTICE_MODELS_OUT,
};
