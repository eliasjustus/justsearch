#!/usr/bin/env node
/**
 * Tempdoc 604 PART V/VI (U7) — generate the LIVE-STREAM liveness window constants into both
 * the backend (Java) and the frontend (TS), so the SSE heartbeat cadence (producer) and the FE
 * re-establishment watchdog window (consumer) CANNOT drift across the two languages.
 *
 * This is the SIBLING of `gen-liveness-constants.mjs` (575 §17 Face A) — NOT an extension of it.
 * The action-lifecycle generator projects a single THREE-value heartbeat-lease window
 * (`heartbeat < displayStale <= reaper`, `displayStale >= 3*heartbeat`); a live-stream window is a
 * TWO-value watchdog model (cadence + watchdog-stale) with a different invariant, so it gets its
 * own generator (PART VI U7: "do not overload the 575 generator").
 *
 * The window VALUES below are the ONE source of truth (no register file — there is no other
 * governance consumer, so the generator itself is the authority, kept drift-proof by the
 * single-authority gate that forbids a hand-authored numeric copy of these constant names).
 *
 *   - Backend producer beats every STREAM_HEARTBEAT_INTERVAL_MS (the agent SSE stream + the
 *     diagnostic SSE controllers).
 *   - FE consumer arms a watchdog of STREAM_WATCHDOG_STALE_MS; no frame within it ⟹ the channel is
 *     dead ⟹ re-establish in place.
 *
 * Generates:
 *   - Java: modules/ui/.../api/StreamLivenessWindows.java          (the heartbeat cadence)
 *   - TS:   modules/ui-web/src/api/generated/stream-liveness-constants.ts (cadence + watchdog window)
 *
 * Usage:
 *   node scripts/codegen/gen-stream-liveness-constants.mjs           # write
 *   node scripts/codegen/gen-stream-liveness-constants.mjs --check   # exit non-zero on drift
 */

import { existsSync, mkdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, join, relative } from 'node:path';
import { fileURLToPath, pathToFileURL } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);
const REPO_ROOT = join(__dirname, '..', '..');

/**
 * The ONE live-stream liveness window. Two values:
 *  - heartbeatMs: how often the backend SSE streams emit a `heartbeat` frame.
 *  - watchdogStaleMs: the FE re-establishment window — no frame (incl. heartbeat) within this and
 *    the channel is presumed dead. Must exceed the heartbeat cadence with margin so ordinary
 *    jitter / a single missed beat does NOT trip a false reconnect (>= 2x heartbeat).
 */
const WINDOW = { heartbeatMs: 15_000, watchdogStaleMs: 40_000 };

const JAVA_OUT = join(
  REPO_ROOT, 'modules', 'ui', 'src', 'main', 'java',
  'io', 'justsearch', 'ui', 'api', 'StreamLivenessWindows.java',
);
const TS_OUT = join(
  REPO_ROOT, 'modules', 'ui-web', 'src', 'api', 'generated', 'stream-liveness-constants.ts',
);

/** Validate the two-value ordering law once, here, at generation. */
function readWindow(window = WINDOW) {
  const { heartbeatMs, watchdogStaleMs } = window;
  for (const [k, v] of Object.entries({ heartbeatMs, watchdogStaleMs })) {
    if (typeof v !== 'number' || !Number.isInteger(v) || v <= 0) {
      throw new Error(`stream-liveness.${k} must be a positive integer (got ${v})`);
    }
  }
  if (!(heartbeatMs < watchdogStaleMs)) {
    throw new Error(
      `stream-liveness ordering violated: require heartbeat(${heartbeatMs}) < watchdogStale(${watchdogStaleMs})`,
    );
  }
  if (!(watchdogStaleMs >= 2 * heartbeatMs)) {
    throw new Error(
      `stream-liveness margin violated: require watchdogStale(${watchdogStaleMs}) >= 2 * heartbeat(${heartbeatMs})`,
    );
  }
  return { heartbeatMs, watchdogStaleMs };
}

/** Java `long` literal with digit-group underscores (e.g. 15000 -> "15_000L"). */
function javaLong(n) {
  return n.toLocaleString('en-US').replace(/,/g, '_') + 'L';
}
/** TS number literal with digit-group underscores (e.g. 40000 -> "40_000"). */
function tsNumber(n) {
  return n.toLocaleString('en-US').replace(/,/g, '_');
}

function renderJava({ heartbeatMs }) {
  return `/* SPDX-License-Identifier: Apache-2.0 */
/*
 * AUTO-GENERATED by scripts/codegen/gen-stream-liveness-constants.mjs (tempdoc 604 PART V/VI).
 *
 * The single source of truth for the LIVE-STREAM SSE heartbeat cadence, shared with the FE
 * re-establishment watchdog (stream-liveness-constants.ts) so producer cadence and consumer window
 * cannot drift. Emitted to BOTH the agent SSE stream (AgentController) and the diagnostic SSE
 * controllers. Do NOT edit by hand (the constant single-authority gate forbids a hand-authored
 * copy); re-generate with:
 *   node scripts/codegen/gen-stream-liveness-constants.mjs
 */
package io.justsearch.ui.api;

/** Generated live-stream liveness constants — see file header. */
public final class StreamLivenessWindows {
  private StreamLivenessWindows() {}

  /** SSE heartbeat cadence: every live SSE stream emits a heartbeat frame at least this often. */
  public static final long STREAM_HEARTBEAT_INTERVAL_MS = ${javaLong(heartbeatMs)};

  /** The same cadence in whole seconds, for the {@code scheduleAtFixedRate(..., SECONDS)} callers. */
  public static final long STREAM_HEARTBEAT_INTERVAL_SECONDS = STREAM_HEARTBEAT_INTERVAL_MS / 1000L;
}
`;
}

function renderTs({ heartbeatMs, watchdogStaleMs }) {
  return `/**
 * AUTO-GENERATED by scripts/codegen/gen-stream-liveness-constants.mjs (tempdoc 604 PART V/VI).
 *
 * The single source of truth for the LIVE-STREAM liveness window, shared with the backend SSE
 * heartbeat cadence (StreamLivenessWindows.java). The FE arms a watchdog of STREAM_WATCHDOG_STALE_MS;
 * any received frame (incl. a backend heartbeat at STREAM_HEARTBEAT_MS cadence) resets it, and an
 * expiry means the channel is dead and must be re-established in place. Do NOT edit by hand;
 * re-generate with:
 *   node scripts/codegen/gen-stream-liveness-constants.mjs
 */

/** Backend SSE heartbeat cadence (informational on the FE — the watchdog window is derived from it). */
export const STREAM_HEARTBEAT_MS = ${tsNumber(heartbeatMs)};

/** FE re-establishment watchdog: no frame within this window ⟹ the channel is dead ⟹ reconnect. */
export const STREAM_WATCHDOG_STALE_MS = ${tsNumber(watchdogStaleMs)};
`;
}

function main() {
  const check = process.argv.includes('--check');
  const window = readWindow();
  const outputs = [
    { path: JAVA_OUT, content: renderJava(window) },
    { path: TS_OUT, content: renderTs(window) },
  ];

  if (check) {
    const drift = [];
    for (const { path, content } of outputs) {
      const before = existsSync(path) ? readFileSync(path, 'utf8') : null;
      if (before !== content) drift.push(relative(REPO_ROOT, path));
    }
    if (drift.length > 0) {
      console.error('[gen-stream-liveness-constants] CHECK FAILED: output drifted from committed files:');
      for (const d of drift) console.error('  - ' + d);
      console.error('Re-run `node scripts/codegen/gen-stream-liveness-constants.mjs`, inspect, commit the regen.');
      process.exit(1);
    }
    console.log('[gen-stream-liveness-constants] check passed — output matches committed files');
    return;
  }

  for (const { path, content } of outputs) {
    mkdirSync(dirname(path), { recursive: true });
    writeFileSync(path, content, 'utf8');
  }
  console.log(
    '[gen-stream-liveness-constants] wrote:\n  ' +
      outputs.map((o) => relative(REPO_ROOT, o.path)).join('\n  '),
  );
}

const isMain = process.argv[1] && import.meta.url === pathToFileURL(process.argv[1]).href;
if (isMain) {
  try {
    main();
  } catch (err) {
    console.error('[gen-stream-liveness-constants] failed:', err);
    process.exit(1);
  }
}

export { JAVA_OUT, TS_OUT, readWindow };
