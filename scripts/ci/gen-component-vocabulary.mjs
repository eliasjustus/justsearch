#!/usr/bin/env node
/**
 * gen-component-vocabulary — codegen the closed presentation-component vocabulary
 * (tempdoc 569 Move 3). The body-axis analogue of gen-token-names: the single
 * authoritative list of host `jf-*` custom elements that a user-authored Presentation
 * Declaration may reference. Projected from the `customElements.define('jf-...')`
 * sites across shell-v0, so a declaration that names a non-host / non-existent
 * component is a type error, not a silent miss — the closed vocabulary the
 * user-authored body composes over (the §2 "bounded semantic vocabulary").
 *
 * Usage:
 *   node scripts/ci/gen-component-vocabulary.mjs           # write the generated file
 *   node scripts/ci/gen-component-vocabulary.mjs --check    # exit 1 if it would change
 */
import {
  readFileSync,
  readdirSync,
  writeFileSync,
  existsSync,
  statSync,
} from 'node:fs';
import { join } from 'node:path';

const SRC_DIR = 'modules/ui-web/src/shell-v0';
const OUT = 'modules/ui-web/src/shell-v0/renderers/component-vocabulary.generated.ts';
// Only host `jf-*` elements are admissible authoring targets (560 PresentationVocabulary).
const DEFINE_RE = /customElements\.define\(\s*['"](jf-[a-z0-9-]*)['"]/g;

const tags = new Set();
// 574 Move 3 — the ATOM subset: generic visual-atom primitives (jf-status-dot, jf-button, …) that
// surfaces/molecules COMPOSE. A file opts in with an `@atom` marker in its header; every jf-* it
// defines is registered as an atom. This is the §21/P6 register-coverage model (the atom set is a
// freshness-checked projection of the define sites), NOT a blanket ban on raw badge/button markup.
const atomTags = new Set();
const ATOM_MARKER = /@atom\b/;

function walk(dir) {
  for (const entry of readdirSync(dir)) {
    const p = join(dir, entry);
    const st = statSync(p);
    if (st.isDirectory()) {
      walk(p);
    } else if (
      p.endsWith('.ts') &&
      !p.endsWith('.test.ts') &&
      !p.endsWith('.generated.ts')
    ) {
      const src = readFileSync(p, 'utf8');
      const isAtomFile = ATOM_MARKER.test(src);
      let m;
      while ((m = DEFINE_RE.exec(src))) {
        tags.add(m[1]);
        if (isAtomFile) atomTags.add(m[1]);
      }
    }
  }
}
walk(SRC_DIR);

const sorted = [...tags].sort();
const sortedAtoms = [...atomTags].sort();
const union = sorted.map((n) => `  | '${n}'`).join('\n');
const literals = sorted.map((n) => `  '${n}',`).join('\n');
const atomLiterals = sortedAtoms.map((n) => `  '${n}',`).join('\n');

const body = `/**
 * AUTO-GENERATED by scripts/ci/gen-component-vocabulary.mjs — DO NOT EDIT.
 *
 * The closed presentation-component vocabulary (tempdoc 569 Move 3): every host
 * jf-* custom element, i.e. the set a user-authored Presentation Declaration may
 * reference. Regenerate with \`node scripts/ci/gen-component-vocabulary.mjs\` after
 * adding a host element. A declaration that names a component outside this union is
 * a type error, not a silent miss.
 */

export type ComponentTag =
${union};

export const COMPONENT_TAGS: readonly ComponentTag[] = [
${literals}
];

/** Type guard: is \`tag\` a member of the closed host component vocabulary? */
export function isComponentTag(tag: string): tag is ComponentTag {
  return (COMPONENT_TAGS as readonly string[]).includes(tag);
}

/**
 * The ATOM subset (tempdoc 574 Move 3): generic visual-atom primitives that surfaces/molecules
 * compose (a status dot, a skinned button, a status badge). A file opts in with an \`@atom\` header
 * marker. This is the register-coverage form (§21/P6): the single source for "what the atom tier is."
 */
export const ATOM_TAGS: readonly ComponentTag[] = [
${atomLiterals}
];

/** Is \`tag\` a registered visual atom (574 Move 3)? */
export function isAtomTag(tag: string): tag is ComponentTag {
  return (ATOM_TAGS as readonly string[]).includes(tag);
}
`;

if (process.argv.includes('--check')) {
  const current = existsSync(OUT) ? readFileSync(OUT, 'utf8') : '';
  if (current !== body) {
    console.error(
      `gen-component-vocabulary --check FAIL: ${OUT} is stale (${sorted.length} components). ` +
        `Run: node scripts/ci/gen-component-vocabulary.mjs`,
    );
    process.exit(1);
  }
  console.log(
    `gen-component-vocabulary OK — ${OUT} matches (${sorted.length} components).`,
  );
} else {
  writeFileSync(OUT, body);
  console.log(
    `gen-component-vocabulary: wrote ${OUT} (${sorted.length} components).`,
  );
}
