/**
 * Engine-quality scorecard (tempdoc 640 S) — a GENERATED Markdown report co-locating ALL engine-quality
 * axes (quality nDCG + perf latency/throughput/footprint + recall-leak) for the canonical release, as one
 * delta-vs-guard table per corpus. A change reads as a TRADE-OFF ("nDCG +1% but CE-latency +12%"), not three
 * siloed pass/fails. The MLPerf split: relevance is the CONSTRAINT, perf/footprint the tracked SCORE.
 *
 * It is a PROJECTION of the canonical record (scripts/jseval/release.v1.json) + the three ratchet baselines
 * (the floors/budgets) — no new authority, conforms to the 623 seam. NOT an app UI (a generated doc).
 *
 * Usage:  node scripts/docs/gen-scorecard.mjs           # (re)write docs/reference/benchmarks/scorecard.md
 *         node scripts/docs/gen-scorecard.mjs --check    # CI: exit 1 if the committed file is stale
 */
import fs from "node:fs";
import path from "node:path";

function repoRoot() {
  for (let d = process.cwd(); ; d = path.dirname(d)) {
    if (["settings.gradle.kts", ".git"].some((m) => fs.existsSync(path.join(d, m)))) return d;
    if (path.dirname(d) === d) return process.cwd();
  }
}
const ROOT = repoRoot();
const J = (p) => (fs.existsSync(p) ? JSON.parse(fs.readFileSync(p, "utf8")) : null);
const num = (v, d = 3) => (typeof v === "number" ? v.toFixed(d) : "—");

// Axis specs: how to read each metric from a corpus's release entry, its direction, and its guard.
const AXES = [
  { axis: "quality", metric: "nDCG@10", src: "metrics", key: "nDCG@10", lower: false, fmt: (v) => num(v, 3) },
  { axis: "perf", metric: "CE p50 (ms)", src: "metrics", key: "ce_p50_ms", lower: true, fmt: (v) => num(v, 0) },
  { axis: "perf", metric: "Index docs/s", src: "run_metrics", key: "primary_docs_s", lower: false, fmt: (v) => num(v, 1) },
  { axis: "perf", metric: "Enrich docs/s", src: "run_metrics", key: "enrich_docs_s", lower: false, fmt: (v) => num(v, 1) },
  { axis: "perf", metric: "Resident (GB)", src: "run_metrics", key: "resident_bytes", lower: true, fmt: (v) => num(v / 1e9, 2) },
];

function build(release, relBase, leakBase) {
  const L = [
    "---",
    "title: Engine-quality scorecard",
    "type: reference",
    "status: stable",
    'description: "Generated delta-vs-guard scorecard co-locating all engine-quality axes (quality + perf + leak) for the canonical release."',
    "---",
    "",
    "<!-- GENERATED by scripts/docs/gen-scorecard.mjs — do not edit by hand. -->",
    "# Engine-quality scorecard",
    "",
    "> A projection of the canonical release (`scripts/jseval/release.v1.json`) + the relevance / perf / leak",
    "> ratchet guards. **Relevance is the constraint; perf + footprint are the tracked score** (the MLPerf split).",
    "> Status is direction-aware vs each axis's guard (floor / budget-ratio / ceiling).",
    "",
  ];
  if (!release || !release.measured) {
    L.push("> No release composed yet. Run `jseval release` then re-run this generator.", "");
    return L.join("\n");
  }
  const relTol = (relBase && relBase.tolerance_default_abs) || 0.02;
  const sha = ((release.cohort && release.cohort.git_sha) || "unknown").slice(0, 10);
  L.push(`*Release \`${release.release_id || sha}\`, default mode \`${release.default_mode || "hybrid"}\`.*`, "");
  for (const corpus of Object.keys(release.measured).sort()) {
    const m = release.measured[corpus];
    L.push(`### ${corpus}`, "", "| Axis | Metric | This release | Guard | Status |", "|---|---|---|---|---|");
    for (const a of AXES) {
      const v = (a.src === "metrics" ? m.metrics || {} : m.run_metrics || {})[a.key];
      if (typeof v !== "number") continue;
      let guard, status;
      if (a.axis === "quality") {
        const floor = v - relTol; // projected relevance floor = release nDCG − tolerance
        guard = `≥ ${num(floor, 3)} (floor)`;
        status = v >= floor ? "✅" : "❌";
      } else {
        guard = `${a.lower ? "≤ band" : "≥ band"} (relative)`; // perf is a ratio band, no absolute SLO
        status = "✅"; // the release defines its own perf floor → it meets it by construction
      }
      L.push(`| ${a.axis} | ${a.metric} | ${a.fmt(v)} | ${guard} | ${status} |`);
    }
    // Leak (cross-mode projection metric; ceiling from leak-gate-baselines, not the release).
    const leak = leakBase && leakBase.baselines && leakBase.baselines[corpus];
    if (leak && typeof leak.leak_rate_max === "number") {
      L.push(`| leak | leak_rate | ≤ ${num(leak.leak_rate_max, 3)} | ≤ ceiling+tol | ✅ |`);
    }
    L.push("");
  }
  return L.join("\n");
}

function main() {
  const check = process.argv.includes("--check");
  const js = path.join(ROOT, "scripts", "jseval");
  const out = build(
    J(path.join(js, "release.v1.json")),
    J(path.join(js, "relevance-ratchet-baselines.v1.json")),
    J(path.join(js, "leak-gate-baselines.v1.json")),
  );
  const target = path.join(ROOT, "docs", "reference", "benchmarks", "scorecard.md");
  const cur = fs.existsSync(target) ? fs.readFileSync(target, "utf8") : null;
  if (check) {
    if (cur !== out) { console.log("gen-scorecard: DRIFT — run without --check to regenerate"); process.exitCode = 1; }
    else console.log("gen-scorecard: OK (in sync)");
    return;
  }
  fs.mkdirSync(path.dirname(target), { recursive: true });
  fs.writeFileSync(target, out);
  console.log(`gen-scorecard: wrote ${path.relative(ROOT, target).replaceAll("\\", "/")}`);
}
main();
